// 导航切换功能
document.addEventListener('DOMContentLoaded', function() {
    const navLinks = document.querySelectorAll('.nav-link');
    const cards = document.querySelectorAll('.card');
    const detailModal = document.getElementById('detailModal');
    const modalClose = document.getElementById('modalClose');
    const viewDetailBtns = document.querySelectorAll('.view-details-btn');
    const cardsWhole = document.querySelectorAll('.card');
    const mobileNavToggle = document.getElementById('mobileNavToggle');
    const mainNav = document.getElementById('mainNav');
    
    // 移动端导航切换
    if (mobileNavToggle && mainNav) {
        mobileNavToggle.addEventListener('click', function() {
            this.classList.toggle('active');
            mainNav.classList.toggle('active');
            
            // 切换图标
            const icon = this.querySelector('i');
            if (this.classList.contains('active')) {
                icon.classList.remove('fa-th-large');
                icon.classList.add('fa-times');
            } else {
                icon.classList.remove('fa-times');
                icon.classList.add('fa-th-large');
            }
        });

        // 点击导航链接后关闭菜单
        navLinks.forEach(link => {
            link.addEventListener('click', () => {
                if (window.innerWidth <= 768) {
                    mobileNavToggle.classList.remove('active');
                    mainNav.classList.remove('active');
                    const icon = mobileNavToggle.querySelector('i');
                    icon.classList.remove('fa-times');
                    icon.classList.add('fa-th-large');
                }
            });
        });

        // 点击外部关闭菜单
        document.addEventListener('click', function(e) {
            if (window.innerWidth <= 768 && 
                !mainNav.contains(e.target) && 
                !mobileNavToggle.contains(e.target) && 
                mainNav.classList.contains('active')) {
                mobileNavToggle.classList.remove('active');
                mainNav.classList.remove('active');
                const icon = mobileNavToggle.querySelector('i');
                icon.classList.remove('fa-times');
                icon.classList.add('fa-th-large');
            }
        });
    }
    
    // 导航点击事件
    navLinks.forEach(link => {
        link.addEventListener('click', function(e) {
            e.preventDefault();
            
            // 更新活动导航项
            navLinks.forEach(item => item.classList.remove('active'));
            this.classList.add('active');
            
            const category = this.getAttribute('data-category');
            
            // 筛选显示卡片
            cards.forEach(card => {
                if (category === 'all' || card.getAttribute('data-category') === category) {
                    card.style.display = 'block';
                    setTimeout(() => {
                        card.style.opacity = '1';
                        card.style.transform = 'translateY(0) scale(1)';
                    }, 10);
                } else {
                    card.style.opacity = '0';
                    card.style.transform = 'translateY(20px) scale(0.95)';
                    setTimeout(() => {
                        card.style.display = 'none';
                    }, 300);
                }
            });
        });
    });
    
    // 卡片点击打开详情
    cardsWhole.forEach(card => {
        card.addEventListener('click', function(e) {
            // 防止点击详情按钮时触发两次
            if (!e.target.closest('.view-details-btn')) {
                const cardId = this.getAttribute('data-id');
                openDetailModal(cardId);
            }
        });
    });
    
    // 查看详情按钮点击
    viewDetailBtns.forEach(btn => {
        btn.addEventListener('click', function(e) {
            e.preventDefault();
            e.stopPropagation(); // 阻止事件冒泡
            const card = this.closest('.card');
            const cardId = card.getAttribute('data-id');
            openDetailModal(cardId);
        });
    });
    
    // 打开详情模态框
    function openDetailModal(cardId) {
        const data = detailData[cardId];
        
        if (data) {
            document.getElementById('modalImg').src = data.img;
            document.getElementById('modalCategory').textContent = data.category;
            document.getElementById('modalTitle').textContent = data.title;
            document.getElementById('modalDescription').textContent = data.description;
            document.getElementById('metaLabel1').textContent = data.meta1;
            document.getElementById('metaValue1').textContent = data.metaValue1;
            document.getElementById('metaLabel2').textContent = data.meta2;
            document.getElementById('metaValue2').textContent = data.metaValue2;
            document.getElementById('modalRating').textContent = data.rating;
            document.getElementById('modalDetails').innerHTML = data.details;
            
            detailModal.classList.add('active');
            document.body.style.overflow = 'hidden'; // 防止背景滚动
        }
    }
    
    // 关闭详情模态框
    modalClose.addEventListener('click', closeModal);
    
    detailModal.addEventListener('click', function(e) {
        if (e.target === detailModal) {
            closeModal();
        }
    });
    
    // 键盘ESC关闭模态框
    document.addEventListener('keydown', function(e) {
        if (e.key === 'Escape' && detailModal.classList.contains('active')) {
            closeModal();
        }
    });
    
    function closeModal() {
        detailModal.classList.remove('active');
        document.body.style.overflow = 'auto';
    }
    
    // 初始化卡片动画
    cards.forEach((card, index) => {
        card.style.opacity = '0';
        card.style.transform = 'translateY(20px) scale(0.95)';
        card.style.transition = 'opacity 0.5s ease, transform 0.5s ease';
        
        setTimeout(() => {
            card.style.opacity = '1';
            card.style.transform = 'translateY(0) scale(1)';
        }, 100 + index * 100);
    });
});
